
public class TestaMes {
	public static void main(String[] args) {
		int mes = 7;
		
		switch (mes) {
		case 1:
			System.out.println("O mês de Janeiro");
			break;
			
		case 2:
			System.out.println("O mês de Fevereiro");
			break;
			
		case 3:
			System.out.println("O mês de Março");
			break;
		
		case 4:
			System.out.println("O mês de Abril");
			break;
		
		case 5:
			System.out.println("O mês de Maio");
			break;
		
		case 6:
			System.out.println("O mês de Junho");
			break;
		
		case 7:
			System.out.println("O mês de Julho");
			break;
		
		case 8:
			System.out.println("O mês de Agosto");
			break;
		
		case 9:
			System.out.println("O mês de Setembro");
			break;
			
		case 10:
			System.out.println("O mês de Outubro");
			break;
		
		case 11:
			System.out.println("O mês de Novembro");
			break;
		
		case 12:
			System.out.println("O mês de Dezembro");
			break;

		default:
			System.out.println("Mês inválido");
			break;
		}
	}
}
